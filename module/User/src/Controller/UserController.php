namespace User\Controller;

use User\Entity\User;
use User\Form\RegistrationForm;
use Zend\Mvc\Controller\AbstractActionController;
use Zend\Mvc\MvcEvent;
use Zend\Stdlib\RequestInterface as Request;
use Zend\Stdlib\ResponseInterface as Response;
use Zend\View\Model\ViewModel;
use Zend\Paginator\Paginator;
use DoctrineORMModule\Paginator\Adapter\DoctrinePaginator as DoctrineAdapter;
use Doctrine\ORM\Tools\Pagination\Paginator as ORMPaginator;

class UserController extends AbstractActionController
{
    /**
     * Менеджер сущностей.
     * @var Doctrine\ORM\EntityManager
     */
    private $entityManager;

    /**
     * Менеджер постов.
     * @var User\Service\UserManager
     */
    private $userManager;

    /**
     * Конструктор, используемый для внедрения зависимостей в контроллер.
     */
    public function __construct($entityManager, $userManager)
    {
        $this->entityManager = $entityManager;
        $this->userManager = $userManager;
    }

    public function dispatch(Request $request, Response $response = null)
    {
        $this->user = $this->entityManager->getRepository(User::class)
            ->findOneByLogin($this->identity());
        if (!$this->user) {
            $this->user = $this->entityManager->getRepository(User::class)
                ->findOneByEmail($this->identity());
        }
        return parent::dispatch($request, $response); // TODO: Change the autogenerated stub
    }

    public function onDispatch(MvcEvent $e)
    {
        $response = parent::onDispatch($e); // TODO: Change the autogenerated stub
        return $response;
    }

    public function registryAction()
    {
        $form = new RegistrationForm();

        if ($this->getRequest()->isPost()) {
            $data = $this->params()->fromPost();
            $form->setData($data);

            if ($form->isValid()) {
                $data = $form->getData();

                if ($data['password'] != $data['re-password']) {
                    $form->get('re-password')->setMessages(['Passwords do not match']);
                } else {
                    $result = $this->userManager->addNewUser($data);

                    if ($result == null) {
                        return $this->redirect()->toRoute('user');
                    }
                    if ($result['login']) {
                        $form->get('login')->setMessages([$result['login']]);
                    }
                    if ($result['email']) {
                        $form->get('email')->setMessages([$result['email']]);
                    }
                }
            }
        }

        return new ViewModel([
            'form' => $form
        ]);

    }

    public function indexAction()
    {
        $page = $this->params()->fromQuery('page', 1);

        $query = $this->entityManager->getRepository(User::class)
            ->findAllUsers();
        $adapter = new DoctrineAdapter(new ORMPaginator($query, false));
        $paginator = new Paginator($adapter);
        $paginator->setDefaultItemCountPerPage(8);
        $paginator->setCurrentPageNumber($page);

        return new ViewModel([
            'users' => $paginator,
        ]);
    }

    public function blockAction()
    {
        $blockedUserId = $this->params()->fromPost('userId', -1);
        $blockedUser = $this->entityManager->getRepository(User::class)
            ->findOneById($blockedUserId);
        if ($blockedUser == null || $this->user == null) {
            $this->getResponse()->setStatusCode(404);
            return;
        }
        $this->userManager->addUserToBlocked($this->user, $blockedUser);
        echo "ok";
        return $this->getResponse();
    }

    public function unblockAction()
    {
        $blockedUserId = $this->params()->fromPost('userId', -1);
        $blockedUser = $this->entityManager->getRepository(User::class)
            ->findOneById($blockedUserId);
        if ($blockedUser == null || $this->user == null) {
            $this->getResponse()->setStatusCode(404);
            return;
        }
        $this->userManager->deleteUserFromBlocked($this->user, $blockedUser);
        echo "ok";
        return $this->getResponse();
    }
}
